-- V1__init_schema.sql
-- Schema only — seed data handled by Spring Boot DataSeeder.java

CREATE TABLE IF NOT EXISTS accounts (
    vpa         VARCHAR(100) PRIMARY KEY,
    holder_name VARCHAR(200) NOT NULL,
    balance     NUMERIC(19, 2) NOT NULL CHECK (balance >= 0),
    pin_hash    VARCHAR(64)  NOT NULL DEFAULT 'notset',
    is_active   BOOLEAN NOT NULL DEFAULT TRUE,
    version     BIGINT NOT NULL DEFAULT 0,
    created_at  TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS transactions (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    packet_hash    VARCHAR(64)  NOT NULL UNIQUE,
    sender_vpa     VARCHAR(100) NOT NULL,
    receiver_vpa   VARCHAR(100) NOT NULL,
    amount         NUMERIC(19, 2) NOT NULL CHECK (amount > 0),
    signed_at      TIMESTAMP NOT NULL,
    settled_at     TIMESTAMP NOT NULL,
    bridge_node_id VARCHAR(200) NOT NULL,
    hop_count      INT NOT NULL DEFAULT 0,
    status         VARCHAR(20)  NOT NULL,
    reject_reason  VARCHAR(200)
);

CREATE INDEX IF NOT EXISTS idx_tx_sender   ON transactions(sender_vpa);
CREATE INDEX IF NOT EXISTS idx_tx_receiver ON transactions(receiver_vpa);
CREATE INDEX IF NOT EXISTS idx_tx_status   ON transactions(status);

CREATE TABLE IF NOT EXISTS app_users (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username   VARCHAR(100) NOT NULL UNIQUE,
    password   VARCHAR(255) NOT NULL,
    role       VARCHAR(50)  NOT NULL DEFAULT 'ROLE_BRIDGE',
    is_active  BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT NOW()
);
